    <h2>Pilates &amp; Yoga studio GRACILIS</h2>
    <img class="float-r" src="studio.jpg" alt="Studio gracilis" width="500" height="350">
    
    <p>Do pre nekoliko godina GRACILIS je bio samo jedan od butnih mišića.
    Simpatičan, zgodan, vitak ali mišić... Danas je GRACILIS fitnes studio
    za pilates i jogu u čije sam temelje ugradila svoja znanja i sertifikate
    iz oblasti Pilatesa, Joge i funkcionalnog fitnesa, sticane u zemlji i
    inostranstvu prethodnih godina. Zahvaljujući tome i činjenici da je učenje
    jedan kontinuirani proces u kome učenici rastu zajedno sa učiteljicom,
    danas delim zadovoljstvo mnogih istrajnih polaznika ovih programa uz čiju
    pomoć su oni ne samo ispravili držanje, oslobodili se viška kilograma,
    preoblikovalili svoje telo, već i podigli nivo samopouzdanja, oslobodili
    se napetosti i popravili raspoloženje... Svesna da je svaki dolazak na
    trening mnogima pobeda nad samim sobom, radujem se svakom postignutom,
    vidljivom i merljivom rezultatu, željno iščekujući nove izazove.
    </p>
